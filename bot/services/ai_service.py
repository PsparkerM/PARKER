from bot.config import ANTHROPIC_API_KEY

SYSTEM_PROMPT = """Ты — P.A.R.K.E.R., персональный AI-нутрициолог и тренер.
Отвечай ТОЛЬКО на русском языке. Будь конкретен: давай реальные продукты, граммовки и время приёмов пищи.
При графике 16ч+ предлагай только быстрые перекусы (до 3 минут). При проблемах со здоровьем — строго соблюдай ограничения.
Всегда заканчивай дисклеймером: «⚠️ Проконсультируйся с врачом перед началом программы»."""


async def generate_meal_plan(profile: dict, macros: dict) -> str:
    if ANTHROPIC_API_KEY:
        return await _claude_plan(profile, macros)
    return _template_plan(profile, macros)


async def _claude_plan(profile: dict, macros: dict) -> str:
    try:
        import anthropic
        client = anthropic.AsyncAnthropic(api_key=ANTHROPIC_API_KEY)
        health = ", ".join(profile.get("health_issues", [])) or "нет"
        msg = await client.messages.create(
            model="claude-sonnet-4-6",
            max_tokens=1500,
            system=SYSTEM_PROMPT,
            messages=[{"role": "user", "content": (
                f"Составь план питания на день.\n"
                f"Цель: {profile.get('goal')} | Пол: {profile.get('gender')} | "
                f"Возраст: {profile.get('age')} | Рост: {profile.get('height_cm')} см | "
                f"Вес: {profile.get('weight_kg')} кг\n"
                f"График работы: {profile.get('schedule')}\n"
                f"Проблемы со здоровьем: {health}\n"
                f"КБЖУ: {macros['calories']} ккал | Б:{macros['protein_g']}г "
                f"Ж:{macros['fat_g']}г У:{macros['carb_g']}г\n\n"
                f"Адаптируй количество и время приёмов пищи под рабочий график."
            )}],
        )
        return msg.content[0].text
    except Exception:
        return _template_plan(profile, macros)


